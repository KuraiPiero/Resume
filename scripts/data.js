class personalInfo {
   constructor(name, title, skills, languages, description) {
      this.name = name
      this.title = title
      this.skills = skills
      this.languages = languages
      this.description = description
   }
}

class professionalInfo {
   constructor(
      title,
      companyName,
      intensity,
      initialDate,
      finalDate,
      place,
      description,
      activities,
      aditionalNotes
   ) {
      this.title = title
      this.companyName = companyName
      this.intensity = intensity
      this.initialDate = initialDate
      this.finalDate = finalDate
      this.place = place
      this.description = description
      this.activities = activities
      this.aditionalNotes = aditionalNotes
   }
}
